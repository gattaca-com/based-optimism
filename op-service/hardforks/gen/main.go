func main() {
	inputFile := flag.String("input", "", "Input file containing the enum definition (required)")
	typeName := flag.String("type", "", "Name of the enum type to process (required)")

	flag.Parse()

	ext := filepath.Ext(*inputFile)
	base := strings.TrimSuffix(filepath.Base(*inputFile), ext)
	outputFile := filepath.Join(filepath.Dir(*inputFile), base+"_gen"+ext)

	buf := new(bytes.Buffer)
	if err := Codegen(*inputFile, *typeName, buf); err != nil {
		log.Fatalf("failed to generate code: %v", err)
	}

	f, err := os.OpenFile(outputFile, os.O_CREATE|os.O_WRONLY|os.O_TRUNC, 0644)
	if err != nil {
		log.Fatalf("failed to open output file: %v", err)
	}
	defer f.Close()

	if _, err := f.Write(buf.Bytes()); err != nil {
		log.Fatalf("failed to write output file: %v", err)
	}
}

func Codegen(inputFile string, typeName string, out io.Writer) error {
	// Validate required flags
	if inputFile == "" {
		return errors.New("must specify input file")
	}
	if typeName == "" {
		return errors.New("must specify type name")
	}

	// Parse the input file to extract the enum
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, inputFile, nil, parser.ParseComments)
	if err != nil {
		log.Fatalf("Failed to parse file %s: %v", inputFile, err)
	}

	// Extract package name
	packageName := f.Name.Name

	// Extract enum values
	var enumValues []string
	for _, decl := range f.Decls {
		genDecl, ok := decl.(*ast.GenDecl)
		if !ok || genDecl.Tok != token.CONST {
			continue
		}

		// Check if this const block contains our enum type
		foundEnum := false
		var currentType string

		for _, spec := range genDecl.Specs {
			vSpec, ok := spec.(*ast.ValueSpec)
			if !ok {
				continue
			}

			// Check for type declaration
			if vSpec.Type != nil {
				if typeIdent, ok := vSpec.Type.(*ast.Ident); ok {
					currentType = typeIdent.Name
					if currentType == typeName {
						foundEnum = true
					}
				}
			}

			// If we've found our enum type, collect values
			if foundEnum && len(vSpec.Names) > 0 {
				// Check for iota or continuation of enum values
				if len(vSpec.Values) > 0 {
					// Check for iota
					if basicLit, ok := vSpec.Values[0].(*ast.BasicLit); ok && basicLit.Value == "iota" {
						// This is the start of an iota sequence
						for _, name := range vSpec.Names {
							enumValues = append(enumValues, name.Name)
						}
					} else if currentType == "" || currentType == typeName {
						// This is a continuation with explicit values
						enumValues = append(enumValues, vSpec.Names[0].Name)
					}
				} else if currentType == "" || currentType == typeName {
					// This is a continuation without values (implicit iota)
					enumValues = append(enumValues, vSpec.Names[0].Name)
				}
			}
		}
	}

	if len(enumValues) == 0 {
		return fmt.Errorf("no enum values found for type %s in file %s", typeName, inputFile)
	}

	// Create template with custom function
	tmpl := template.New("enum_gen")
	tmpl.Funcs(template.FuncMap{
		"lower": strings.ToLower,
	})
	tmpl, err = tmpl.Parse(codeTemplate)
	if err != nil {
		log.Fatalf("Failed to parse template: %v", err)
	}

	// Prepare data for template
	data := struct {
		Package  string
		TypeName string
		Values   []string
	}{
		Package:  packageName,
		TypeName: typeName,
		Values:   enumValues,
	}

	buf := new(bytes.Buffer)
	if err := tmpl.Execute(buf, data); err != nil {
		return fmt.Errorf("failed to execute template: %v", err)
	}

	formattedCode, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("Failed to format generated code: %v", err)
	}

	if _, err := out.Write(formattedCode); err != nil {
		return fmt.Errorf("failed to write generated code: %v", err)
	}

	return nil
}
